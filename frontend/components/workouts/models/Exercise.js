class Exercise {
    constructor(
        id,
        name,
        numOfSets,
        numOfReps,
        duration,
        weight,
        rest,
    ) {
        this.id = id;
        this.name = name;
        this.numOfSets = numOfSets;
        this.numOfReps = numOfReps;
        this.duration = duration;
        this.weight = weight;
        this.rest = rest;
        
    }
}

export default Exercise;